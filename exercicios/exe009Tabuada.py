numero = int(input('Digite um número'))
print('Tabuada')
print('Adição')
soma = 0
while soma <= 10:
    soma = soma + 1
    resultado = numero + soma
    print('{} + {} = {}'.format(numero, soma, resultado))

print('Subtração')
soma = 0
while soma <= 10:
    soma = soma + 1
    resultado = numero - soma
    print('{} - {} = {}'.format(numero, soma, resultado))

print('Multiplicação:')
soma = 0
while soma <= 10:
    soma = soma + 1
    resultado = numero * soma
    print('{} * {} = {}'.format(numero, soma, resultado))





